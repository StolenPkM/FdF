#include "libft.h"

int		ft_word_count(char const *s, char c)
{
	int i;
	int ret;

	ret = 0;
	i = 0;
	while (s[i])
	{
		if (s[i] != c && (s[i - 1] == c || i == 0))
			ret++;
		i++;
	}
	return (ret);
}
