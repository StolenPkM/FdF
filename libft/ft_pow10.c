#include "libft.h"

int	ft_pow10(int pw)
{
	int i;
	int n;

	i = 1;
	n = 1;
	if (pw == 0)
		return (1);
	while (i <= pw)
	{
		n *= 10;
		i++;
	}
	return (n);
}
