#include "libft.h"

char	*ft_strcat(char *dest, const char *src)
{
	int i;
	int j;

	i = 0;
	while (dest[i] != '\0')
		i++;
	j = i;
	i = 0;
	while (src[i] != '\0')
		dest[j++] = src[i++];
	dest[j] = '\0';
	return (dest);
}
